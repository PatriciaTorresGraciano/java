package br.com.generation.exerciciosPOO.exercicio06;

public class ContaBancaria {

	private String banco;
	private String tipoConta;
	private String agencia;
	private String numeroConta;
	private String nomeDestino;
	private String cpfDestino;
	public String getBanco() {
		return banco;
	}
	public void setBanco(String banco) {
		this.banco = banco;
	}
	public String getTipoConta() {
		return tipoConta;
	}
	public void setTipoConta(String tipoConta) {
		this.tipoConta = tipoConta;
	}
	public String getAgencia() {
		return agencia;
	}
	public void setAgencia(String string) {
		this.agencia = string;
	}
	public String getNumeroConta() {
		return numeroConta;
	}
	public String setNumeroConta(String numeroConta) {
		return this.numeroConta = numeroConta;
	}
	public String getNomeDestino() {
		return nomeDestino;
	}
	public void setNomeDestino(String nomeDestino) {
		this.nomeDestino = nomeDestino;
	}
	public String getCpfDestino() {
		return cpfDestino;
	}
	public void setCpfDestino(String cpfDestino) {
		this.cpfDestino = cpfDestino;
	}
	
	
	
}
